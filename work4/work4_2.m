hFig = figure(4);
set(hFig,'name','1 hidden layers , 3 hidden nodes,learning rate=0.1, error>0.07 && iteration<10000 ','Position', [300 0 500 400]);
N=200;
theta1 = linspace(-180,180, N)*pi/360;
r = 8
x1 = -5 + r*sin(theta1)+randn(1,N);
y1 = r*cos(theta1)+randn(1,N);
x2 = 5 + r*sin(theta1)+randn(1,N);
y2 = -r*cos(theta1)+randn(1,N);

hold on;
axis equal;
plot(x1,y1,'bo');
plot(x2,y2,'rs');

alpha=0.1;
W1=[0.1 0.2 0.3]';
W2=[0.1 0.1 0.2]';
W3=[0.2 0.1 0.1]';

W21=[0.1 0.1 0.1 0.1]';
W22=[0.1 0.3 0.2 0.1]';

it=0;
error=10;
while(error>0.05 && it<10000)
    it=it+1;
    error=0;
for i=1:400    
    if i<201
        X=[x1(i) y1(i) 1]';  
        d=[1 0];        
    else
        X=[x2(i-200) y2(i-200) 1]';  
        d=[0 1];        
    end
   
   S1=W1'*X;
   S2=W2'*X;
   S3=W3'*X;
  
   o1=1.0/(1.0+exp(-S1));
   o2=1.0/(1.0+exp(-S2));
   o3=1.0/(1.0+exp(-S3));
   
   S21=o1*W21(1)+o2*W21(2)+o3*W21(3)+W21(4);
   S22=o1*W22(1)+o2*W22(2)+o3*W22(3)+W22(4);
   
   
   o21=1.0/(1.0+exp(-S21));
   o22=1.0/(1.0+exp(-S22));
   
   
   error=error+sqrt((d(1)-o21)*(d(1)-o21))+sqrt((d(2)-o22)*(d(2)-o22));
   W212=W21;
   W222=W22;
   
    W21(1)=W21(1)+alpha*(d(1)-o21)*o21*(1-o21)*(o1);
   W21(2)=W21(2)+alpha*(d(1)-o21)*o21*(1-o21)*(o2);
   W21(3)=W21(3)+alpha*(d(1)-o21)*o21*(1-o21)*(o3);
   W21(4)=W21(4)+alpha*(d(1)-o21)*o21*(1-o21)*(1);
   
   W22(1)=W22(1)+alpha*(d(2)-o22)*o22*(1-o22)*(o1);
   W22(2)=W22(2)+alpha*(d(2)-o22)*o22*(1-o22)*(o2);
   W22(3)=W22(3)+alpha*(d(2)-o22)*o22*(1-o22)*(o3);
   W22(4)=W22(4)+alpha*(d(2)-o22)*o22*(1-o22)*(1);
   
   %W1---------------------------------------------
   a=(d(1)-o21)*o21*(1-o21)*W212(1);
   b=(d(2)-o22)*o22*(1-o22)*W222(1);  
   
   W1(1)=W1(1)+alpha*(a+b)*(o1)*(1-o1)*(X(1));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(1);
   b=(d(2)-o22)*o22*(1-o22)*W222(1);  
   
   W1(2)=W1(2)+alpha*(a+b)*(o1)*(1-o1)*(X(2));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(1);
   b=(d(2)-o22)*o22*(1-o22)*W222(1);   
   
   W1(3)=W1(3)+alpha*(a+b)*(o1)*(1-o1)*(X(3));
   
   %W2--------------------------------------------   
   a=(d(1)-o21)*o21*(1-o21)*W212(2);
   b=(d(2)-o22)*o22*(1-o22)*W222(2);   
   
   W2(1)=W2(1)+alpha*(a+b)*(o2)*(1-o2)*(X(1));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(2);
   b=(d(2)-o22)*o22*(1-o22)*W222(2);
   
   W2(2)=W2(2)+alpha*(a+b)*(o2)*(1-o2)*(X(2));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(2);
   b=(d(2)-o22)*o22*(1-o22)*W222(2);
   
   W2(3)=W2(3)+alpha*(a+b)*(o2)*(1-o2)*(X(3));
   
    %W3--------------------------------------------   
   a=(d(1)-o21)*o21*(1-o21)*W212(3);
   b=(d(2)-o22)*o22*(1-o22)*W222(3);
   
   W3(1)=W3(1)+alpha*(a+b)*(o3)*(1-o3)*(X(1));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(3);
   b=(d(2)-o22)*o22*(1-o22)*W222(3);
   
   W3(2)=W3(2)+alpha*(a+b)*(o3)*(1-o3)*(X(2));
   
   a=(d(1)-o21)*o21*(1-o21)*W212(3);
   b=(d(2)-o22)*o22*(1-o22)*W222(3);
   
   W3(3)=W3(3)+alpha*(a+b)*(o3)*(1-o3)*(X(3));   
   
end


error=error/400;
EIT(1,it)=it;
EIT(2,it)=error;
end

x=linspace(-20,20);
y=(-W1(1)*x-W1(3))/W1(2);
L=plot(x,y);
set(L,'Color','red');

x=linspace(-20,20);
y=(-W2(1)*x-W2(3))/W2(2);
L=plot(x,y);
set(L,'Color','green');

x=linspace(-20,20);
y=(-W3(1)*x-W3(3))/W3(2);
L=plot(x,y);
set(L,'Color','blue');

for i=1:50
    for j=1:50
        x=-20+i*0.8;
        y=-20+j*0.8;
        S1=x*W1(1)+y*W1(2)+W1(3);
        S2=x*W2(1)+y*W2(2)+W2(3);
        S3=x*W3(1)+y*W3(2)+W3(3);        
     
   o1=1.0/(1.0+exp(-S1));
   o2=1.0/(1.0+exp(-S2));
   o3=1.0/(1.0+exp(-S3));
   
   S21=o1*W21(1)+o2*W21(2)+o3*W21(3)+W21(4);
   S22=o1*W22(1)+o2*W22(2)+o3*W22(3)+W22(4);
   
   o21=1.0/(1.0+exp(-S21));
   o22=1.0/(1.0+exp(-S22));
   
        
    if o21>0.5
        plot(x,y,'b +');
    else
        plot(x,y,'r +');
    end
    end
end
axis([-20 20 -20 20]);

hFig = figure(5);
set(hFig,'name','error','Position', [300 0 500 400]);
line(EIT(1,:),EIT(2,:));


